    // Text3
    display.setTextColor(1);
    display.setTextWrap(false);
    display.setFont(&Nokia_Cellphone_FC_8);
    display.setCursor(32, 43);
    display.print("Aim at");

    // >
    display.drawBitmap(77, 23, image___1_bits, 3, 5, 1);

